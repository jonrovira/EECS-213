/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30  
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
    int division = x >> n;

    int sign = x >> 31;
    int negN = ~n + 1;
    int shift = 31 + negN;
    int oneOnLeft = 0x1 << 31;
    int check = ~(oneOnLeft >> shift);

    int add = !!(x & check) & sign;

    return division + add;
}
